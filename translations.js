// ─── Engine ───────────────────────────────────────────────────────────────────

const I18N_STORAGE_KEY = 'noesis-lang';
const DEFAULT_LANG = 'en';

function getCurrentLang() {
  return localStorage.getItem(I18N_STORAGE_KEY) || DEFAULT_LANG;
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  localStorage.setItem(I18N_STORAGE_KEY, lang);
  applyTranslations(lang);
  updateToggleButton(lang);
  document.documentElement.lang = lang;
}

function t(key, lang) {
  const locale = lang || getCurrentLang();
  return (TRANSLATIONS[locale] && TRANSLATIONS[locale][key]) ||
         (TRANSLATIONS[DEFAULT_LANG] && TRANSLATIONS[DEFAULT_LANG][key]) ||
         key;
}

function applyTranslations(lang) {
  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key, lang);
    if (val) el.innerHTML = val;
  });

  // Placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key, lang);
    if (val) el.placeholder = val;
  });

  // Title attribute
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = t(key, lang);
    if (val) el.title = val;
  });

  // Alt attribute (images)
  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.getAttribute('data-i18n-alt');
    const val = t(key, lang);
    if (val) el.alt = val;
  });

  // Aria-label
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    const val = t(key, lang);
    if (val) el.setAttribute('aria-label', val);
  });

  // <meta> description
  const metaDesc = document.querySelector('meta[data-i18n-content]');
  if (metaDesc) {
    const key = metaDesc.getAttribute('data-i18n-content');
    const val = t(key, lang);
    if (val) metaDesc.setAttribute('content', val);
  }

  // <title> tag
  const titleKey = document.documentElement.getAttribute('data-i18n-title');
  if (titleKey) {
    const val = t(titleKey, lang);
    if (val) document.title = val;
  }
}

function updateToggleButton(lang) {
  const btn = document.getElementById('lang-toggle');
  if (!btn) return;
  // Show the ALTERNATE language as what you'd switch TO
  const labels = { en: '🇮🇹 IT', it: '🇬🇧 EN' };
  const nextLang = lang === 'en' ? 'it' : 'en';
  btn.innerHTML = labels[nextLang] || nextLang.toUpperCase();
  btn.setAttribute('data-current-lang', lang);
  btn.title = lang === 'en' ? 'Passa all\'italiano' : 'Switch to English';
}

function toggleLang() {
  const current = getCurrentLang();
  const next = current === 'en' ? 'it' : 'en';
  setLang(next);
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLang();
  applyTranslations(lang);
  updateToggleButton(lang);
  document.documentElement.lang = lang;
});

// Expose globals
window.noesisI18n = { setLang, toggleLang, getCurrentLang, t };
